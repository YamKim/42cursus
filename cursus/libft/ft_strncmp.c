#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	n += 1;
	while ((*s1 || *s2) && --n)
	{
		if (*s1 != *s2)
			break ;
		++s1;
		++s2;
	}
	if (!n)
		return (0);
	return ((int)(*s1 - *s2));
}
